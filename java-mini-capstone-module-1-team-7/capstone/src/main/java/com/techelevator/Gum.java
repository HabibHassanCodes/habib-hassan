package com.techelevator;

public class Gum {
    //att
    private int price = 200;
    private String gumName = "sourCreamGUM";
    private String gumMessage = "Chew Chew, Yum!";


    //constructor
    public Gum() {
        this.price = price;
        this.gumName = gumName;
        this.gumMessage = gumMessage;
    }


    //getters
    public int getPrice() {
        return price;
    }

    public String getGumName() {
        return gumName;
    }

    public String getGumMessage() { return gumMessage; }

}





